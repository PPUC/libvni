constexpr uint8_t kRcon[11] = {0x00, 0x01, 0x02, 0x04, 0x08, 0x10,
                               0x20, 0x40, 0x80, 0x1b, 0x36};

uint8_t xtime(uint8_t x) {
  return (uint8_t)((x << 1) ^ (((x >> 7) & 1) * 0x1b));
}

void sub_bytes(uint8_t* state) {
  for (int i = 0; i < 16; i++) {
    state[i] = kSbox[state[i]];
  }
}

void inv_sub_bytes(uint8_t* state) {
  for (int i = 0; i < 16; i++) {
    state[i] = kRsbox[state[i]];
  }
}

void shift_rows(uint8_t* state) {
  uint8_t tmp;
  tmp = state[1];
  state[1] = state[5];
  state[5] = state[9];
  state[9] = state[13];
  state[13] = tmp;

  tmp = state[2];
  state[2] = state[10];
  state[10] = tmp;
  tmp = state[6];
  state[6] = state[14];
  state[14] = tmp;

  tmp = state[3];
  state[3] = state[15];
  state[15] = state[11];
  state[11] = state[7];
  state[7] = tmp;
}

void inv_shift_rows(uint8_t* state) {
  uint8_t tmp;
  tmp = state[13];
  state[13] = state[9];
  state[9] = state[5];
  state[5] = state[1];
  state[1] = tmp;

  tmp = state[2];
  state[2] = state[10];
  state[10] = tmp;
  tmp = state[6];
  state[6] = state[14];
  state[14] = tmp;

  tmp = state[3];
  state[3] = state[7];
  state[7] = state[11];
  state[11] = state[15];
  state[15] = tmp;
}

void mix_columns(uint8_t* state) {
  for (int i = 0; i < 4; i++) {
    int idx = i * 4;
    uint8_t a = state[idx];
    uint8_t b = state[idx + 1];
    uint8_t c = state[idx + 2];
    uint8_t d = state[idx + 3];
    uint8_t e = a ^ b ^ c ^ d;
    state[idx] ^= e ^ xtime(a ^ b);
    state[idx + 1] ^= e ^ xtime(b ^ c);
    state[idx + 2] ^= e ^ xtime(c ^ d);
    state[idx + 3] ^= e ^ xtime(d ^ a);
  }
}

void inv_mix_columns(uint8_t* state) {
  for (int i = 0; i < 4; i++) {
    int idx = i * 4;
    uint8_t a = state[idx];
    uint8_t b = state[idx + 1];
    uint8_t c = state[idx + 2];
    uint8_t d = state[idx + 3];

    uint8_t a2 = xtime(a);
    uint8_t b2 = xtime(b);
    uint8_t c2 = xtime(c);
    uint8_t d2 = xtime(d);

    uint8_t a4 = xtime(a2);
    uint8_t b4 = xtime(b2);
    uint8_t c4 = xtime(c2);
    uint8_t d4 = xtime(d2);

    uint8_t a8 = xtime(a4);
    uint8_t b8 = xtime(b4);
    uint8_t c8 = xtime(c4);
    uint8_t d8 = xtime(d4);

    uint8_t a9 = a8 ^ a;
    uint8_t b9 = b8 ^ b;
    uint8_t c9 = c8 ^ c;
    uint8_t d9 = d8 ^ d;

    uint8_t a11 = a8 ^ a2 ^ a;
    uint8_t b11 = b8 ^ b2 ^ b;
    uint8_t c11 = c8 ^ c2 ^ c;
    uint8_t d11 = d8 ^ d2 ^ d;

    uint8_t a13 = a8 ^ a4 ^ a;
    uint8_t b13 = b8 ^ b4 ^ b;
    uint8_t c13 = c8 ^ c4 ^ c;
    uint8_t d13 = d8 ^ d4 ^ d;

    uint8_t a14 = a8 ^ a4 ^ a2;
    uint8_t b14 = b8 ^ b4 ^ b2;
    uint8_t c14 = c8 ^ c4 ^ c2;
    uint8_t d14 = d8 ^ d4 ^ d2;

    state[idx] = a14 ^ b11 ^ c13 ^ d9;
    state[idx + 1] = a9 ^ b14 ^ c11 ^ d13;
    state[idx + 2] = a13 ^ b9 ^ c14 ^ d11;
    state[idx + 3] = a11 ^ b13 ^ c9 ^ d14;
  }
}

void add_round_key(uint8_t* state, const uint8_t* round_key) {
  for (int i = 0; i < 16; i++) {
    state[i] ^= round_key[i];
  }
}

void key_expansion(const uint8_t* key, uint8_t* round_keys) {
  memcpy(round_keys, key, 16);
  uint32_t bytes_generated = 16;
  uint8_t rcon_iter = 1;
  uint8_t temp[4];

  while (bytes_generated < 176) {
    for (int i = 0; i < 4; i++) {
      temp[i] = round_keys[bytes_generated - 4 + i];
    }

    if (bytes_generated % 16 == 0) {
      uint8_t t = temp[0];
      temp[0] = temp[1];
      temp[1] = temp[2];
      temp[2] = temp[3];
      temp[3] = t;

      for (int i = 0; i < 4; i++) {
        temp[i] = kSbox[temp[i]];
      }

      temp[0] ^= kRcon[rcon_iter++];
    }

    for (int i = 0; i < 4; i++) {
      round_keys[bytes_generated] = round_keys[bytes_generated - 16] ^ temp[i];
      bytes_generated++;
    }
  }
}

void aes_decrypt_block(uint8_t* output, const uint8_t* input,
                       const uint8_t* round_keys) {
  uint8_t state[16];
  memcpy(state, input, 16);

  add_round_key(state, round_keys + 160);
  for (int round = 9; round >= 1; round--) {
    inv_shift_rows(state);
    inv_sub_bytes(state);
    add_round_key(state, round_keys + round * 16);
    inv_mix_columns(state);
  }
  inv_shift_rows(state);
  inv_sub_bytes(state);
  add_round_key(state, round_keys);

  memcpy(output, state, 16);
}

